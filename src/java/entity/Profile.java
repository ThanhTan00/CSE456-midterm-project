package entity;

/**
 *
 * @author ADMIN
 */
public class Profile extends Account{
    
    private String gender;
    private String phone;
    private String address;
    private String avatar;
    
    public Profile(String gender, String phone, String address, String avatar, int id, String email, String password, String name, int isAdmin) {
        super(id, email, password, name, isAdmin);
        this.gender = gender;
        this.phone = phone;
        this.address = address;
        this.avatar = avatar;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getAddress() {
        return address;
    }

    public void setAddress(String address) {
        this.address = address;
    }

    public String getAvatar() {
        return avatar;
    }

    public void setAvatar(String avatar) {
        this.avatar = avatar;
    }
    
    
}
